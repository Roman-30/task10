package ru.vsu.cs.task10.console;

public class ArrayPoints {
    double[] x;
    double[] y;

    public ArrayPoints(double[] x, double[] y) {
        this.x = x;
        this.y = y;
    }

    public double[] getX() {
        return x;
    }

    public double[] getY() {
        return y;
    }
}
